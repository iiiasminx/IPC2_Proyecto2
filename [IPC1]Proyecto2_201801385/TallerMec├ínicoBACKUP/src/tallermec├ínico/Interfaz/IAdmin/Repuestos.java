    private void BotonModificar1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BotonModificar1ActionPerformed

        if (jTextField1.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Elige un cosito de la lista xfa :c");
        } else{

            try{

                System.out.println("Modificando");
                int linea = jTable1.getSelectedRow();

                System.out.println(linea);
                TallerMecánico.l3.obtenerr(linea);

                // aquí ando creando mi nuevo objeto

                String ids = jTable1.getModel().getValueAt(linea, 0).toString();
                
                int id = Integer.parseInt(ids);
                int existencias = Integer.parseInt(jTextField4.getText());
                
                float precio = Float.parseFloat(jTextField5.getText());

                
                ObjRepuestos s1 = new ObjRepuestos(id, jTextField1.getText(),
                    jTextField2.getText(), jTextField3.getText(), existencias,
                precio);

                // reemplazando
                
                TallerMecánico.l2.modificar(linea, s1);
                TallerMecánico.l2.obtenerr(linea);

                // metiendo las cosas en la tabla

                jTable1.setValueAt(jTextField1.getText(),jTable1.getSelectedRow(), 1);
                jTable1.setValueAt(jTextField2.getText(),jTable1.getSelectedRow(), 2);
                jTable1.setValueAt(jTextField3.getText(),jTable1.getSelectedRow(), 3);
                jTable1.setValueAt(jTextField4.getText(),jTable1.getSelectedRow(), 4);
                jTable1.setValueAt(jTextField5.getText(),jTable1.getSelectedRow(), 5);

                // regresandolo todo

                BotonModificar.setVisible(false);
                jTextField1.setText("");
                jTextField2.setText("");
                jTextField3.setText("");
                jTextField4.setText("");
                jTextField5.setText("");

                System.out.println("");

            } catch(Exception e){
                System.out.println(e);
            }
        }

    }//GEN-LAST:event_BotonModificar1ActionPerformed

