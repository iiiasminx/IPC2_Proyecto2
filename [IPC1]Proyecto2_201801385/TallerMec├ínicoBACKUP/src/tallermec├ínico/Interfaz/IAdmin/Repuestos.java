    private void meterATabla() {
               
        try {
           int contador = 0;
           
           TallerMecánico.l2.imprimir();
           System.out.println(TallerMecánico.l2.tamanio());
           
            while (contador < TallerMecánico.l2.tamanio()) {
                
            TallerMecánico.l2.obtenerr(contador);
            
            ObjRepuestos encontrado = TallerMecánico.l2.buscado.getElemento();
            
            TallerMecánico.tablitaRepuestos.addRow(new Object[]{
                encontrado.getId(), encontrado.getNombre(), encontrado.getMarca(),
            encontrado.getModelo(), encontrado.getExistencias(), encontrado.getPrecio()});    
            contador++;
        }

       } catch (Exception e) {
            System.out.println(e);
        }
    
    }
